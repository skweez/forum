package net.skweez.forum.model;

/**
 * User class.
 * 
 * @author elm
 * 
 */
public class User {
	/**
	 * the id
	 */
	private int id;

	/**
	 * the name
	 */
	private String name;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
